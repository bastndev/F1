export interface PersonalMistakeResult {
	text: string;
	corrections: number;
}

type CompiledMistake = {
	pattern: RegExp;
	correction: string;
};

const compiledMistakes: CompiledMistake[] = Object.entries(PERSONAL_MISTAKES)
	.sort(([a], [b]) => b.length - a.length)
	.map(([mistake, correction]) => {
		const escaped = escapeRegExp(mistake);
		const pattern = mistake.includes(' ')
			? new RegExp(escaped, 'gi')
			: new RegExp(`\\b${escaped}\\b`, 'gi');

		return { pattern, correction };
	});

/**
 * Apply personal writing patterns before any other correction layer.
 * Handles both single-word and multi-word patterns sorted by length
 * to avoid partial replacements.
 */
export function applyPersonalMistakes(text: string): string {
	return applyPersonalMistakesWithStats(text).text;
}

export function applyPersonalMistakesWithStats(text: string): PersonalMistakeResult {
	let result = text;
	let corrections = 0;

	for (const { pattern, correction } of compiledMistakes) {
		result = result.replace(pattern, (match) => {
			const next = preserveCase(match, correction);
			if (next !== match) {
				corrections++;
			}

			return next;
		});
	}

	return { text: result, corrections };
}

function escapeRegExp(string: string): string {
	return string.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function preserveCase(original: string, replacement: string): string {
	if (original === original.toUpperCase()) {
		return replacement.toUpperCase();
	}
	if (original[0] === original[0].toUpperCase()) {
		return replacement[0].toUpperCase() + replacement.slice(1);
	}
	return replacement;
}
